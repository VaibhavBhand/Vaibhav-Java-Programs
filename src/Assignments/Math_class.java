package Assignments;

public class Math_class 
{

	public static void main(String[] args) 
	{
		System.out.println(Math.PI);
		System.out.println(Math.addExact(100,1525));
		System.out.println(Math.addExact(5151551,5164812));
		System.out.println(Math.subtractExact(100,5));
		System.out.println(Math.floorDiv(100,5));
		System.out.println(Math.sqrt(100));
		System.out.println(Math.multiplyExact(100,100));
		System.out.println(Math.max(500, 8000));
		System.out.println(Math.min(5000, 87979.23));
		System.out.println(Math.random());
	}

}
