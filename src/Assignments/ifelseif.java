package Assignments;


public class ifelseif 
{
	public static void main(String[] args) 
	{
		char a = 'F';
		
		
		if(a=='M')
		{
			System.out.println("Ticket price is 300");
		}
		else if(a=='F')
		{
			System.out.println("Ticket price is 200");
		}
		else
		{
			System.out.println("Ticket price is 100.");
		}
	
	
			
	
				
		
	}
	

}
