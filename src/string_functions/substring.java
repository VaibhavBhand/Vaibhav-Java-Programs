package string_functions;

public class substring {

	public static void main(String[] args)
	{
		String name = "Vaibhav Vishwanath Bhand";
				
				System.out.println(name.substring(8)); //substring with one param
				
				
				System.out.println(name.substring(18,24 ));//substring with two param
						
				System.out.println(name.substring(0, 2)); // it prints 1 less index
	}

}
